async function main() {
  const auth = getAuth();
  const sheetsApi = google.sheets({ version: 'v4', auth });
  const SPREADSHEET_ID = process.env.GOOGLE_SPREADSHEET_ID;

  console.log('\n═══════════════════════════════════════════');
  console.log('  Заполнение «Стратегия» — ExtremeFest S-IDs');
  console.log('═══════════════════════════════════════════\n');

  // 1. Очистить demo-данные стоматологии (строки 4-50)
  console.log('1. Очищаю demo-данные (строки 4-50)...');
  await sheetsApi.spreadsheets.values.clear({
    spreadsheetId: SPREADSHEET_ID,
    range: "'Стратегия'!A4:I50",
  });
  console.log('   ✅ Очищено\n');

  // 2. Записываем 25 S-ID строк начиная с row 4
  console.log('2. Записываю 25 S-ID...');
  await sheetsApi.spreadsheets.values.update({
    spreadsheetId: SPREADSHEET_ID,
    range: `'Стратегия'!A4:I${4 + STRATEGY_ROWS.length - 1}`,
    valueInputOption: 'USER_ENTERED',
    requestBody: { values: STRATEGY_ROWS },
  });
  console.log(`   ✅ Записано ${STRATEGY_ROWS.length} строк (S-01 — S-25)\n`);

  // 3. Обновляем покрытие воронки K4:O8
  console.log('3. Обновляю покрытие воронки (K4:O8)...');
  const funnelData = [
    ['Привлечение', '8', '40%', '40%', '✅ В балансе'],
    ['Прогрев', '8', '40%', '25%', '⚠️ Перекос +15%'],
    ['Продажа', '5', '25%', '20%', '⚠️ Перекос +5%'],
    ['Лояльность', '4', '20%', '15%', '⚠️ Перекос +5%'],
    ['Итого', '25', '', '100%', ''],
  ];
  await sheetsApi.spreadsheets.values.update({
    spreadsheetId: SPREADSHEET_ID,
    range: "'Стратегия'!K4:O8",
    valueInputOption: 'USER_ENTERED',
    requestBody: { values: funnelData },
  });
  console.log('   ✅ Обновлено\n');

  console.log('═══════════════════════════════════════════');
  console.log('✅ Готово!');
  console.log(`SMM: https://docs.google.com/spreadsheets/d/${SPREADSHEET_ID}/edit`);
  console.log('═══════════════════════════════════════════\n');
}

main().catch(err => {
  console.error('Fatal:', err.message);
  process.exit(1);
});
